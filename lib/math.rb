def addition(num1, num2)
  result = num1 + num2
  puts result
end

def subtraction(num1, num2)
  result = num1 - num2
  puts result
end

def division(num1, num2)
  result = num1 / num2
  puts result
end

def multiplication(num1, num2)
  result = num1 * num2
  puts result
end

def modulo(num1, num2)
  result = num1 % num2
  puts result
end

def square_root(num)
  result = Math.sqrt(num)
  puts result
end

